# control flow = code akan berjalan saat sudah memenuhi kondisi tertentu

# if syntax
'''
if some_condition:
    # execute some code
elif some_other_condition:
    # do something different
else: # else syntax
    # do something else 
'''

# contoh
perut = 'kenyang'

if perut == 'lapar':
    print('BERI SAYA MAKAN!')
elif perut == 'kenyang':
    print('Saya tidak lapar')
elif perut == 'sakit':
    print('Beri saya obat')
else:
    print('Saya tidak tau')


