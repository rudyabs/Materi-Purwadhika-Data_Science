# Intergers ("int") -> angka secara keseluruhan

print(type(300))

print(300)

# Floating Point/Float ("float") -> angka dengan koma

print(type(2.3))

print(2.3)

# Strings ("str") -> karakter huruf

print(type("ini adalah string"))

print("ini adalah string")

# Lists ("list") -> objek dengan aturan yang berurutan

print(type([10, "hello", 200.5]))

print([10, "hello", 200.5])

# Dictionaries ("dict") -> nilai key:objek yang berpasangan dan tidak berurutan

print(type({"mykey1":"value1", "mykey2":"value2"}))

print({"mykey1":"value1", "mykey2":"value2"})

# Tuples ("tup") -> objek berurutan dan kekal(immutable): tidak bisa diubah nilainya

print(type((10, "hello", 10.5)))

print((10, "hello", 10.5))
# Sets ("set") -> objuek unik yang tidak berurutan

print(type({"a","b"}))

print({"a","b"})
# Booleans ("bool") -> nilai logika dengan indikasi "True" atau "False"

print(type(True))

print(True)
print(False)