# penggunaan angka(number) pada program python

# jumlah
print("1+1=", 1+1)

# kali
print("2*2=", 2*2)

# kurang
print("2-1=", 2-1)

# bagi
print("4/2=", 4/2) 
#pembagian akan mengasilkan angka dalam bentuk float

# modulo/modulus atau "mod" operator
# berguna saat ingin mengetahui angka ganjil/genap
print(7%4)
print(50%5)

# pangkat
print("2 pangkat 3=", 2**3)

# python akan selalu mengkalkulasi sesuai urutan (order of operations)
